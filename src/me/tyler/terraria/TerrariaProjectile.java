package me.tyler.terraria;

public class TerrariaProjectile {

	private float x;
	private float y;
	private short id;
	private short type;
	
	public float getX() {
		return x;
	}
	
	public float getY() {
		return y;
	}
	
	public short getId() {
		return id;
	}
	
	public short getType() {
		return type;
	}
	
	public void setX(float x) {
		this.x = x;
	}
	
	public void setY(float y) {
		this.y = y;
	}
	
	public void setId(short id) {
		this.id = id;
	}
	
}
